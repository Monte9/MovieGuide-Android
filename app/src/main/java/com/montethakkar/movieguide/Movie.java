public class Movie {

    int id;
    String poster_path;
    String title;
    String overview;
    String backdrop_path;

    public int getId() {
        return id;
    }

    public String getPosterPath() {
        return poster_path;
    }

    public String getTitle() {
        return title;
    }

    public String getOverview() {
        return overview;
    }

    public String getBackdropPath() {
        return backdrop_path;
    }

}
